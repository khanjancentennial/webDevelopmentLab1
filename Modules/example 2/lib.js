export function addTwoValues(addValueOne,addValueTwo){
    return addValueOne + addValueTwo;
    }

export function DeductTwoValues(deductValueOne,deductValueTwo){
    return deductValueOne - deductValueTwo;
    }    

export function MultiplyTwoValues(multiplyValueOne,multiplyValiueTwo){
    return multiplyValueOne * multiplyValiueTwo;
    }

export function devideByTwoValues(devideValueOne,devideValueTwo){
    return devideValueOne / devideValueTwo;
    }